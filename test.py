def hottestDay(files,year):
    dates = list()
    max_temp = list()
    mon_maxdate = list()
    mon_maxtemp = list()
    
    for file in files:
        handle = open(file)
        for line in handle:
            wordlist = line.split(',')
            try:
                dates.append(wordlist[0])
                max_temp.append(int(wordlist[1]))
            except:
                pass
    
        temp = max(max_temp)
        mon_maxtemp.append(temp)
        tempindex = max_temp.index(temp)
        date = dates[tempindex]
        mon_maxdate.append(date)
    
    temp = max(mon_maxtemp)
    tempindex = mon_maxtemp.index(temp)
    date = mon_maxdate[tempindex]
    print(year,'\t',date,'\t',temp)
        
def annual(files,year):
    max_temp = []
    min_temp = []
    max_humidity = []
    min_humidity = []
    
    mon_maxtemp = []
    mon_mintemp = []
    mon_maxhumidity = []
    mon_minhumidity = []
    
    for file in files:
        handle = open(file)
        for line in handle:
            wordlist = line.split(',')
            try:
            #print(wordlist[1])
                max_temp.append(int(wordlist[1]))
                min_temp.append(int(wordlist[3]))
                max_humidity.append(int(wordlist[7]))
                min_humidity.append(int(wordlist[9]))
            
            except:
                pass
    
        mon_maxtemp.append(max(max_temp))
        mon_mintemp.append(min(min_temp))
        mon_maxhumidity.append(max(max_humidity))
        mon_minhumidity.append(min(min_humidity))
        
    
   
    
    print(year,'\t',max(mon_maxtemp), '\t\t',min(mon_mintemp),'\t\t',max(mon_maxhumidity),'\t\t',min(min_humidity))
    

